#include "math.h"

t_comp		ft_mulcomp(t_comp z1, t_comp z2)
{
	t_comp	tmp;

	tmp.r = z1.r * z2.r - z1.i * z2.i;
	tmp.i = z2.r * z1.i + z1.r * z2.i;
	return (tmp);
}
